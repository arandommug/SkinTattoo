OPCODE_NAMES = {
    0x00: "add", 0x01: "and", 0x0F: "dp2", 0x1D: "ge", 0x32: "mad", 0x36: "mov", 0x37: "movc",
    0x38: "mul", 0x45: "sample", 0x4B: "sqrt", 0x4D: "sincos",
}

tokens = [int(t, 16) for t in TOKENS_HEX.split()]
print(f"Total tokens: {len(tokens)} ({len(tokens)*4} bytes)\n")

pos = 0
inst_idx = 0
while pos < len(tokens):
    op_token = tokens[pos]
    opcode = op_token & 0x7FF
    length = (op_token >> 24) & 0x7F
    if length == 0: length = 1
    name = OPCODE_NAMES.get(opcode, f"op_0x{opcode:02X}")
    tokens_slice = tokens[pos:pos + length]
    actual_tokens = ' '.join(f'0x{t:08X}' for t in tokens_slice)
    print(f"  [{inst_idx:2}] pos={pos:3} opcode=0x{opcode:02X}({name:6}) len={length:2} | {actual_tokens}")
    if pos + length > len(tokens):
        print(f"      !! OVERRUN")
        break
    pos += length
    inst_idx += 1

status = "OK" if pos == len(tokens) else "FAIL"
print(f"\n[{status}] {inst_idx} instructions, consumed {pos}/{len(tokens)} tokens.")
